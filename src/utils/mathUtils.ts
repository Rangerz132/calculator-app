export function sum(a: number = 0, b: number = 0): number {
  return a + b;
}

export function substract(a: number = 0, b: number = 0): number {
  return a - b;
}

export function multiply(a: number = 0, b: number = 0): number {
  return a * b;
}

export function divide(a: number = 0, b: number = 0): number {
  return a / b;
}
